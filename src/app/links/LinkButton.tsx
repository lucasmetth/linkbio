/**
 * Componente LinkButton - botão de link premium e acessível
 */
export default function LinkButton({
  label,
  href,
  external = false,
  icon,
  ariaLabel,
}: LinkButtonProps) {
  const sanitizedHref = sanitizeUrl(href);
  const linkProps = external
    ? { target: '_blank', rel: 'noopener noreferrer' }
    : {};

  // Destaque especial para WhatsApp
  const isWhatsApp = icon === 'whatsapp';

  return (
    <a
      href={sanitizedHref}
      aria-label={ariaLabel || label}
      className={`
        group relative flex items-center justify-center gap-3 px-8 py-5
        ${isWhatsApp 
          ? 'bg-gradient-to-r from-green-500/20 via-green-400/15 to-green-500/20 border-green-500/30 hover:border-green-400/50 hover:bg-green-500/25 hover:shadow-green-500/30' 
          : 'bg-gradient-to-r from-amber-500/10 via-yellow-500/5 to-amber-500/10 border-amber-500/20 hover:border-amber-400/40 hover:bg-amber-500/20 hover:shadow-amber-500/20'
        }
        border
        rounded-2xl
        ${isWhatsApp ? 'text-green-50' : 'text-amber-50'}
        font-semibold text-lg tracking-wide
        transition-all duration-300 ease-out
        hover:shadow-lg
        hover:scale-[1.02] hover:-translate-y-0.5
        active:scale-[0.98] active:translate-y-0
        focus-visible:outline-none focus-visible:ring-4 
        ${isWhatsApp 
          ? 'focus-visible:ring-green-500/50 focus-visible:ring-offset-neutral-950' 
          : 'focus-visible:ring-amber-500/50 focus-visible:ring-offset-neutral-950'
        }
        focus-visible:ring-offset-2
        min-h-[56px] touch-manipulation
        backdrop-blur-sm
      `}
      {...linkProps}
    >
      {/* Ícone à esquerda */}
      {icon && (
        <span className={`
          ${isWhatsApp ? 'text-green-400' : 'text-amber-400'}
          transition-transform duration-300 group-hover:scale-110 group-hover:rotate-3
        `}>
          {renderIcon(icon)}
        </span>
      )}

      {/* Label */}
      <span className="relative z-10">{label}</span>

      {/* Badge "Resposta Rápida" no WhatsApp */}
      {isWhatsApp && (
        <span className="absolute -top-2 -right-2 px-2 py-0.5 bg-green-500 text-white text-xs font-bold rounded-full animate-pulse">
          Online
        </span>
      )}

      {/* Indicador de link externo */}
      {external && !isWhatsApp && (
        <svg
          className="w-5 h-5 text-amber-400/60 transition-all duration-300 group-hover:text-amber-300 group-hover:translate-x-1 group-hover:-translate-y-1"
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
          aria-hidden="true"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth={2}
            d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14"
          />
        </svg>
      )}

      {/* Brilho sutil no hover */}
      <span className={`
        absolute inset-0 rounded-2xl bg-gradient-to-r from-transparent to-transparent
        ${isWhatsApp 
          ? 'group-hover:via-green-400/10' 
          : 'group-hover:via-amber-400/10'
        }
        via-transparent transition-all duration-500
      `} />
    </a>
  );
}
